class Solution{
  public:
    int countMin(string A){
    //complete the function here
    string B=A;
        reverse(A.begin(),A.end());
        int n=A.size();
        
        int dp[n+1][n+1];
        for(int i=0;i<=n;i++)
        {
            for(int j=0;j<=n;j++)
            {
                if(i==0 or j==0)
                dp[i][j]=0;
                else if(A[i-1]==B[j-1])
                dp[i][j]=1+dp[i-1][j-1];
                else
                dp[i][j]=max(dp[i-1][j],dp[i][j-1]);
            }
        }
        int len= dp[n][n];
        if(A==B)
        return 0;
        return n-len;
    }
};

